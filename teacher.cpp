//查看所有预约
void Teacher::showallorder()
{
	Orderfile of;
	if (of.m_size == 0)
	{
		cout << "无预约记录！" << endl;
		system("pause");
		system("cls");
		return;
	}
	for (int i = 0; i < of.m_size; i++)
	{
		cout << i + 1 << "、";
		cout << "预约时间：周" << of.m_orderdate[i]["date"];
		cout << "时间段： " << (of.m_orderdate[i]["interval"] == "1" ? "上午" : "下午");
		cout << "学号：" << of.m_orderdate[i]["stuid"];
		cout << "姓名：" << of.m_orderdate[i]["stuname"];
		cout << "机房号： " << of.m_orderdate[i]["roomid"];
		string status = "状态：";
		if (of.m_orderdate[i]["status"] == "1")
		{
			status += "审核中";
		}
		else if (of.m_orderdate[i]["status"] == "2")
		{
			status += "预约成功";
		}
		else if (of.m_orderdate[i]["status"] == "-1")
		{
			status += "预约失败，审核未通过";
		}
		else
		{
			status += "预约已取消";
		}
		cout << status << endl;
	}
	system("pause");
	system("cls");
}
//审核预约
void Teacher::validorder()
{
	Orderfile of;
	if (of.m_size == 0)
	{
		cout << "无预约记录！" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "待审核的预约记录如下：" << endl;
	int index = 1; vector<int>v;
	for (int i = 0; i < of.m_size; i++)
	{
			if (of.m_orderdate[i]["status"] == "1" )
			{
				v.push_back(i);
				cout << index++ << "、";
				cout << "预约时间：周" << of.m_orderdate[i]["date"];
				cout << "时间段： " << (of.m_orderdate[i]["interval"] == "1" ? "上午" : "下午");
				cout << "机房号： " << of.m_orderdate[i]["roomid"];
				string status = "状态：审核中！";
				cout << status << endl;
			}
	}
	cout << "请输入审核的预约记录，0表示返回" << endl;
	int select; int ret = 0;
	while (true)
	{
		cin >> select;
		if (select >= 0 && select <= v.size())
		{
			if (select == 0)
			{
				break;
			}
			else
			{
				cout << "请输入审核结果" << endl;
				cout << "1、通过      2、不通过" << endl;
				cin >> ret;
				if (ret == 1)
				{
					of.m_orderdate[v[select - 1]]["status"] = "2";
				}
				else
				{
					of.m_orderdate[v[select - 1]]["status"] = "-1";
				}
				of.updateorder();
				cout << "审核完毕！" << endl;
				break;
			}
		}
		cout << "输入有误，请重新输入！" << endl;
	}
	system("pause");
	system("cls");
}